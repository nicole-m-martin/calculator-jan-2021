export function addSum(oneNumber, twoNumber) {
    return oneNumber + twoNumber;
}

export function subSum(oneNumber, twoNumber) {
    return oneNumber - twoNumber;
}

export function divideSum(oneNumber, twoNumber) {
    return oneNumber / twoNumber;
}

export function multiSum(oneNumber, twoNumber) {
    return oneNumber * twoNumber;
}